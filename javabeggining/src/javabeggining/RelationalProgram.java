package javabeggining;

public class RelationalProgram {

	public static void main(String[] args) {
		int num1 = 12;
		int num2 = 14;
		System.out.println(num1>num2);
		System.out.println(num1<num2);
		System.out.println(num1<=num2);
		System.out.println(num1>=num2);
		System.out.println(num1==num2);
		System.out.println(num1!=num2);

	}

}
